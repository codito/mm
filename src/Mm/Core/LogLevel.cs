namespace Mm.Core;

public enum LogLevel
{
    /// <summary>
    /// Enable error logging. Default.
    /// </summary>
    Error = 0,

    /// <summary>
    /// Enable warning logging level.
    /// </summary>
    Warning = 1,

    /// <summary>
    /// Enable verbose logs.
    /// </summary>
    Verbose = 2,
}
